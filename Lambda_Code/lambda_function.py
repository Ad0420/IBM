# -*- coding: utf-8 -*-

import logging
import ask_sdk_core.utils as ask_utils
import json

from ask_sdk_core.skill_builder import SkillBuilder
from ask_sdk_core.dispatch_components import AbstractRequestHandler
from ask_sdk_core.dispatch_components import AbstractExceptionHandler
from ask_sdk_core.handler_input import HandlerInput
from ask_sdk_model import Response
import boto3

logger = logging.getLogger(__name__)
logger.setLevel(logging.INFO)

# Initialize the IoT client
iot_client = boto3.client('iot-data', region_name='us-east-1')  # us-east-1 for US East (N. Virginia) region

def send_iot_message(topic, message):
    payload = json.dumps({"message": message})
    response = iot_client.publish(
        topic=topic,
        qos=1,
        payload=payload
    )
    return response

class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        speak_output = "Welcome, you can say Turn on the light or Turn off the light. Which would you like to try?"

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )

class TurnOnLightIntentHandler(AbstractRequestHandler):
    """Handler for Turn On Light Intent."""
    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("ledLight")(handler_input)

    def handle(self, handler_input):
        topic = "lights/command"
        message = "turn on"
        send_iot_message(topic, message)
        speak_output = "Turning on the light."

        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )
        
